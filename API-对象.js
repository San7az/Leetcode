// hasOwnProperty() 方法返回一个布尔值
// 表示对象自有属性（而不是继承来的属性）中是否具有指定的属性。
Object.prototype.hasOwnProperty()

//Object.entries() 静态方法返回一个数组
// 包含给定对象自有的可枚举字符串键属性的键值对。
Object.entries()

// Object.keys() 静态方法返回一个由给定对象自身的可枚举的字符串键属性名组成的数组。
Object.keys()

// Map.prototype.set()
// Map 实例的 set() 方法会向 Map 对象添加或更新一个指定的键值对。
const map1 = new Map()
map1.set('bar', 'foo')

// Map.prototype.has()
// Map 实例的 has() 方法返回一个布尔值，指示键名是否存在。
Map.prototype.has()

//Map 实例的 get() 方法返回该 map 中的指定元素。
Map.prototype.get()

//Map 实例的 size 访问器属性返回此 map 中元素的数量。
Map.prototype.size

//Map 实例的 delete() 方法从该 map 中删除指定键的元素。
Map.prototype.delete()
